
class SwitchDemo {
	public static void main(String[] args){
		char ch = 'C';
		switch(ch) {
			case 'A':
				System.out.println("characte A");
				break;
			case 'B':
				System.out.println("Character B");
				break;
			case 'C':
				System.out.println("Character C");
				break;
			default:
				System.out.println("By Default");
				break;
		}
	}
}


